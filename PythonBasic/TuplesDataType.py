print('Tuples')
#unlike lists we cannot change a tule once we created it
pi_tuple=(3,1,4,1,5,9)
print(pi_tuple)
#converting tuple to list
new_list= list(pi_tuple)
print(new_list)
#converting list to tuple
new_tuple = tuple(new_list)
print(new_tuple)
#lenght of tuple
print(len(new_tuple))
#min & max in tuple
print(min(new_tuple))
print(max(new_tuple))